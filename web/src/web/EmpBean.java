package web;

public class EmpBean {

	private String name = null;
	private int age = 0;

	public int getAge() {
		return age;
	}

	//引数ageをageに代入
	public void setAge(int age) {
		this.age = age;
	}

	public String getName() {
		return name;
	}


	//引数nameをnameに代入する
	public void setName(String name) {
		this.name = name;
	}

}
